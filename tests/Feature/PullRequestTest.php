test('GitHub provider posts the expected payload and returns parsed result', function () {
    Http::fake([
        'api.github.com/repos/datashaman/specify/pulls' => Http::response([
            'html_url' => 'https://github.com/datashaman/specify/pull/42',
            'number' => 42,
            'id' => 12345678,
        ], 201),
    ]);

    $ws = Workspace::factory()->create();
    $repo = Repo::factory()->for($ws)->create([
        'url' => 'https://github.com/datashaman/specify.git',
        'access_token' => 'ghp_secret',
        'default_branch' => 'main',
        'provider' => RepoProvider::Github,
    ]);

    $result = (new GithubPullRequestProvider)->createPullRequest(
        repo: $repo,
        head: 'specify/feat-x',
        base: 'main',
        title: 'Specify: do thing',
        body: 'Generated by agent run',
    );

    expect($result)->toMatchArray([
        'url' => 'https://github.com/datashaman/specify/pull/42',
        'number' => 42,
    ]);

    Http::assertSent(function ($request) {
        $body = $request->data();

        return $request->url() === 'https://api.github.com/repos/datashaman/specify/pulls'
            && $request->hasHeader('Authorization', 'Bearer ghp_secret')
            && $request->hasHeader('Accept', 'application/vnd.github+json')
            && $body['title'] === 'Specify: do thing'
            && $body['head'] === 'specify/feat-x'
            && $body['base'] === 'main';
    });
});

test('GitHub provider parses owner/repo from various URL shapes', function () {
    Http::fake(['*' => Http::response(['html_url' => 'x', 'number' => 1, 'id' => 1])]);

    $ws = Workspace::factory()->create();

    $cases = [
        'https://github.com/owner/repo.git',
        'https://github.com/owner/repo',
    ];

    foreach ($cases as $url) {
        $repo = Repo::factory()->for($ws)->create([
            'url' => $url,
            'access_token' => 'ghp',
            'provider' => RepoProvider::Github,
        ]);
        (new GithubPullRequestProvider)->createPullRequest($repo, 'h', 'b', 't');
    }

    Http::assertSentCount(2);
    Http::assertSent(fn ($r) => str_contains($r->url(), '/owner/repo/pulls'));
});

test('GitHub provider raises when access_token missing', function () {
    $ws = Workspace::factory()->create();
    $repo = Repo::factory()->for($ws)->create([
        'access_token' => null,
        'provider' => RepoProvider::Github,
    ]);

    expect(fn () => (new GithubPullRequestProvider)->createPullRequest($repo, 'h', 'b', 't'))
        ->toThrow(RuntimeException::class, 'access_token');
});

